public class AirTransport extends Transport {
    private int range;
    private double wingspan;

    public int getRange(){
        return range;
    }
    public void setRange(int range){
        this.range = range;
    }

    public double getWingspan(){
        return wingspan;
    }
    public void setWingspan(int wingspan){
        this.wingspan = wingspan;
    }
}
