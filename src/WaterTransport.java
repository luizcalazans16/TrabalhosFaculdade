public class WaterTransport {
    private int beam;
    private int draught;

    public int getBeam(){
        return beam;
    }
    public void setBeam(int beam){
        this.beam = beam;
    }

    public int getDraught(){
        return draught;
    }
    public void setDraught(int draught){
        this.draught = draught;
    }
}


